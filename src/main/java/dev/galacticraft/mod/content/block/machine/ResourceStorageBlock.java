package dev.galacticraft.mod.content.block.machine;

import dev.galacticraft.machinelib.api.block.SimpleMachineBlock;
import net.minecraft.resources.ResourceKey;
import net.minecraft.resources.ResourceLocation;
import net.minecraft.world.item.context.BlockPlaceContext;
import net.minecraft.world.level.block.Block;
import net.minecraft.world.level.block.entity.BlockEntityType;
import net.minecraft.world.level.block.state.BlockState;
import net.minecraft.world.level.block.state.StateDefinition;
import net.minecraft.world.level.block.state.properties.IntegerProperty;
import net.minecraft.world.level.block.state.properties.Property;
import org.jetbrains.annotations.NotNull;

public class ResourceStorageBlock extends SimpleMachineBlock {
    public static final Property<Integer> AMOUNT = IntegerProperty.create("amount", 0, 8);

    public ResourceStorageBlock(Properties settings, ResourceLocation id) {
        super(settings, id);
    }

    public ResourceStorageBlock(Properties settings, ResourceKey<BlockEntityType<?>> key) {
        super(settings, key);
    }

    @Override
    protected void createBlockStateDefinition(StateDefinition.Builder<Block, BlockState> builder) {
        super.createBlockStateDefinition(builder);
        builder.add(AMOUNT);
    }

    @Override
    public BlockState getStateForPlacement(@NotNull BlockPlaceContext context) {
        return super.getStateForPlacement(context).setValue(AMOUNT, 0);
    }
}
